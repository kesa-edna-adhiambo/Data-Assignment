#Creating a simple stack
my_stack = []
my_stack.append('edna')
my_stack.append('faith')
my_stack.append('kame')
print(my_stack)

#Accessing item in stack without removing
my_stack = ['apple', 'mango', 'banana', 'melon', 'orange']
print(my_stack[2])

#adding item in stack
my_stack.append('guava')
print(my_stack)

#removing item from stack
my_stack.pop()
print(my_stack)

